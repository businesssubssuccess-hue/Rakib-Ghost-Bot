module.exports = {
  config: {
    name: "hacker2",
    aliases: ["hacker2","phonehack","syshack"],
    version: "2.8",
    author: "Rakib",
    countDown: 5,
    role: 1, 
    shortDescription: "Fake hacking prank",
    category: "prank",
    guide: { en: "{p}hacker [reply/mention] [u] | {p}hacker loop [u] | {p}hacker stop" }
  },

  onStart: async function ({ message, args, event, api }) {
    const { threadID, mentions, senderID } = event;

    if (args[0] === "stop") {
      if (global.hackerLoops.has(threadID)) {
        clearInterval(global.hackerLoops.get(threadID));
        global.hackerLoops.delete(threadID);
        return message.reply("⭕ হ্যাকিং প্রোটোকল টার্মিনেট করা হয়েছে।");
      }
      if (global.hackerWait.has(threadID)) {
        global.hackerWait.delete(threadID);
        return message.reply("⭕ হ্যাকার মোড অফ করা হয়েছে।");
      }
      return message.reply("⚠️ কোনো প্রসেস চালু নেই।");
    }

    const isUnsend = args.some(arg => arg.toLowerCase() === "u");
    const isLoop = args.includes("loop");

    let targetID;
    if (event.type === "message_reply") {
      targetID = event.messageReply.senderID;
    } else if (Object.keys(mentions).length > 0) {
      targetID = Object.keys(mentions)[0];
    } else if (args[0] && /^\d+$/.test(args[0])) {
      targetID = args[0];
    } else {
      return message.reply("⚠️ কাউকে মেনশন দিন বা রিপ্লাই করুন।");
    }

    try {
      const info = await api.getUserInfo(targetID);
      const targetName = info[targetID].name;

      if (isLoop) {
        if (global.hackerLoops.has(threadID)) return message.reply("⚠️ অলরেডি লুপ চলছে!");
        
        message.reply(`💀 GHOST-NET HACKING: @${targetName}...`);
        
        const intervalId = setInterval(() => {
          const line = HACK_LINES[Math.floor(Math.random() * HACK_LINES.length)];
          api.sendMessage({
            body: `@${targetName} ${line}`,
            mentions: [{ tag: `@${targetName}`, id: targetID }]
          }, threadID, (err, msgInfo) => {
             if (isUnsend && !err && msgInfo) {
               setTimeout(() => api.unsendMessage(msgInfo.messageID), 3000);
             }
          });
        }, 2000);

        global.hackerLoops.set(threadID, intervalId);
      } else {
        global.hackerWait.set(threadID, { targetID, targetName, isUnsend });
        const line = HACK_LINES[Math.floor(Math.random() * HACK_LINES.length)];
        return api.sendMessage({
          body: `@${targetName} ${line}\n\n🕵️‍♂️ তুই এখন রাকিব বসের জালে!`,
          mentions: [{ tag: `@${targetName}`, id: targetID }]
        }, threadID, (err, msgInfo) => {
           if (isUnsend && !err && msgInfo) {
             setTimeout(() => api.unsendMessage(msgInfo.messageID), 3000);
           }
        });
      }
    } catch (e) {
      return message.reply("❌ ইউজার পাওয়া যায়নি।");
    }
  },

  onReply: async function ({ event, api }) {
    const { threadID, senderID } = event;
    if (!global.hackerWait.has(threadID)) return;

    const data = global.hackerWait.get(threadID);
    if (senderID == data.targetID) {
      const line = HACK_LINES[Math.floor(Math.random() * HACK_LINES.length)];
      api.sendMessage({
        body: `[CRITICAL] @${data.targetName} ${line}`,
        mentions: [{ tag: `@${data.targetName}`, id: data.targetID }]
      }, threadID, (err, msgInfo) => {
         if (data.isUnsend && !err && msgInfo) {
           setTimeout(() => api.unsendMessage(msgInfo.messageID), 3000);
         }
      });
    }
  }
};
